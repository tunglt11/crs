package com.crs.entity;

import java.util.Date;

public class Customer {
	long customerId;
	String customerName;
	String yearOfBird;
	String email;
	String phone;
	String address;
	String drivingLicenceNo;
	String licenceIssuedBy;
	Date licenceIssuedDate;
	String identifierNumber;
	String identifierIssuedBy;
	Date identifierIssuedDate;
	String userName;
	String password;
	public long getCustomerId() {
		return customerId;
	}
	public void setCustomerId(long customerId) {
		this.customerId = customerId;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getYearOfBird() {
		return yearOfBird;
	}
	public void setYearOfBird(String yearOfBird) {
		this.yearOfBird = yearOfBird;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDrivingLicenceNo() {
		return drivingLicenceNo;
	}
	public void setDrivingLicenceNo(String drivingLicenceNo) {
		this.drivingLicenceNo = drivingLicenceNo;
	}
	public String getLicenceIssuedBy() {
		return licenceIssuedBy;
	}
	public void setLicenceIssuedBy(String licenceIssuedBy) {
		this.licenceIssuedBy = licenceIssuedBy;
	}
	public Date getLicenceIssuedDate() {
		return licenceIssuedDate;
	}
	public void setLicenceIssuedDate(Date licenceIssuedDate) {
		this.licenceIssuedDate = licenceIssuedDate;
	}
	public String getIdentifierNumber() {
		return identifierNumber;
	}
	public void setIdentifierNumber(String identifierNumber) {
		this.identifierNumber = identifierNumber;
	}
	public String getIdentifierIssuedBy() {
		return identifierIssuedBy;
	}
	public void setIdentifierIssuedBy(String identifierIssuedBy) {
		this.identifierIssuedBy = identifierIssuedBy;
	}
	public Date getIdentifierIssuedDate() {
		return identifierIssuedDate;
	}
	public void setIdentifierIssuedDate(Date identifierIssuedDate) {
		this.identifierIssuedDate = identifierIssuedDate;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
}
